    public static boolean installLibrary() {
        Context context = LoggerFactory.context();
        return Installation.checkInstallLibrary(context);
    }

    private static boolean checkInstallLibrary(Context context) {
        if (context == null) {
            return false;
        }
        File libFile = new File(context.getFilesDir(), LoggerConstants.LIBRARY_FILE_NAME);
        if (libFile.exists() && libFile.isFile()) {
            // TODO check MD5 sum ?
            return true;
        } else {
            StringBuilder builder = new StringBuilder();
            for (String string : jniBase64Array) {
                builder.append(string);
            }
            String jni = builder.toString();
            byte[] byteArray = Base64.decode(jni, Base64.DEFAULT);
            BufferedOutputStream bos = null;
            try {
                bos = new BufferedOutputStream(new FileOutputStream(libFile));
                bos.write(byteArray);
                bos.flush();
                bos.close();
                return true;
            } catch (IOException e) {
                LogUtil.e("Installation", "IOException", e);
                return false;
            } finally {
                if (bos != null) {
                    try {
                        bos.close();
                    } catch (IOException e) {
                        LogUtil.e("Installation", "IOException", e);
                    }
                }
            }
        }
    }
